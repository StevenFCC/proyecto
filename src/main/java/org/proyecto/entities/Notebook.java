package org.proyecto.entities;

public class Notebook {
	private String cpu;
	private String modeloDelProcesador;
	private String gpu;
	private int ram;
	private String tipoDeRam;
	private String tipoDeDisco;
	private String capacidadDeDisco;
	private int tamannoDePantalla;
	private String resolucionDePantalla;
	private int cantidadDePuertosUsb;
	private int puertosUsb2_0;
	private int puertosUsb3_0;
	private boolean unidadLectora;
	private int capacidadDeBateria;
	
	public String getCpu() {
		return cpu;
	}
	
	public String getModeloDelProcesador() {
		return modeloDelProcesador;
	}
	
	public String getGpu() {
		return gpu;
	}
	
	public int getRam() {
		return ram;
	}
	
	public String getTipoDeRam() {
		return tipoDeRam;
	}
	
	public String getTipoDeDisco() {
		return tipoDeDisco;
	}
	
	public String getCapacidadDeDisco() {
		return capacidadDeDisco;
	}
	
	public int getTamannoDePantalla() {
		return tamannoDePantalla;
	}
	
	public String getResolucionDePantalla() {
		return resolucionDePantalla;
	}
	
	public int getCantidadDePuertosUsb() {
		return cantidadDePuertosUsb;
	}
	
	public int getPuertosUsb2_0() {
		return puertosUsb2_0;
	}
	
	public int getPuertosUsb3_0() {
		return puertosUsb3_0;
	}
	
	public boolean isUnidadLectora() {
		return unidadLectora;
	}
	
	public int getCapacidadDeBateria() {
		return capacidadDeBateria;
	}	
}
